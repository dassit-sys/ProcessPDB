//Test functionality of readPDB()
void test_readPDB() {	
	int *num = (int *)malloc(sizeof(int)*5);

	//Create the test file for readPDB() (copy of 1atb.pdb, with 62 residues)
	createTestFile();
	
	//Check that the file was read correctly and 62 residues are present
	FILE *testFile;
	testFile = fopen("testFile.pdb", "r"); //Open in read mode
	Residue *residues = readPDB(testFile, num);

	//Check that 62 residues have a valid construction 
	bool hasError = false;
	int count = 1;
	for (int x = 0; x < 62; x++) { //For each residue
		Residue resTemp = residues[x];	
		Atom *atomsTemp = resTemp.atoms;

		for (int y = 0; y < resTemp.atomNum; y++) { //For each atom
			if ((resTemp.atoms[y].atomIndex != count) || strcmp(resTemp.atoms[y].chain, "A") != 0) {
				//Mark as having an error if above conditions not met
				hasError = true;
				count++;
			} else {
				count++;
			}
		}
	}
	
	//Assert 62 valid Residues were created from reading 1atb.pdb
	CU_ASSERT(!hasError); //Check that no mistakes were found
	CU_ASSERT(count == 912); //Check that there were 912 atoms
	
	//Clean up test file
	cleanTestFile();
	
}

